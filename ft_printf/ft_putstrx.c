#include "ft_printf.h"

void	ft_putstrx(char *s1, char **s2, int *count)
{
	if (s1 == NULL)
		s1 = "(null)";
	while (*s1)
	{
		write (1, s1, 1);
		(*count)++;
		s1++;
	}
	(*s2) += 2;
}
